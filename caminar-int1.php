               quien quiero llegar.
            </p>
            <p>¿Cuál es la fuerza que me impulsa e invita a entrar en el campo de la comunicación? La
               vida, ese soplo de aliento que magnifica el milagro a través de lo que te comunicas y
               comunicas a quienes te rodean; esto tiene una maravillosa implicación en el otro, en el
               campo social del que formamos parte y que nos permite experimentar la existencia en las
               interrelaciones de manera grata, simple y edificante.
            </p>
