            </p>
            <p>
               ¿Cómo elaboramos el tejido del mensaje, cómo lo transmitimos, cómo conectamos y nos
               integramos en los códigos de ese mensaje, cómo lo interpretamos en sincronía?, esa
               respuesta, es el camino que nos dirige a una relación exitosa, con tu propia persona y con
               los demás.
            </p>
            <p>Muchas veces se permite a la frustración, la impotencia, la rabia, tomar el campo de
               acción de nuestras relaciones, con nuestros padres, pareja, nuestros hijos, familiares, los
               jefes , empleados, amigos y demás, frente al no sentirte comprendido, y a su vez
               aceptado (integrado con el otro), llevándonos a una desintegración contrario al propósito
               inicial de tu mensaje. ¿Cuál es el propósito, cómo identificarlo?, es una destreza que
               podrás adquirir mediante tu disposición y trabajo personal. Eres lo más importante que
               tienes, ocuparte de tu autocuidado y desarrollo, es una condición esencial entre tu ser y tu
               estar
            </p>
            <p>Es una realidad que poseemos capacidades mentales que nos permiten construir,
               inventar, aprender, interpretar conceptos y expresarlos mediante el uso de señales o
               sonidos.
            </p>
            <p>Todo en el ser humano comunica, y todo está asociado a como sientes dentro tuyo el
               mensaje, como autor o como emisor.
            </p>
            <p>Todo lo que se transmite tiene una raíz profunda en el ser, se cultiva en la necesidad, y
               fácilmente pasamos de manera automática al deseo de ser escuchados, entendidos y
               recibir una respuesta en ello; descuidando el celo en nuestras emociones. 
            </p>
            <p>Digo a otros, pero no me integro con lo que estoy diciendo, esto ante la ausencia de
               dialogo interno; el hablar a mi propia persona, abre un plano inmenso de comprensión 
